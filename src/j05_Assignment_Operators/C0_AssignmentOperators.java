package j05_Assignment_Operators;

public class C0_AssignmentOperators {
    public static void main(String[] args) {
        //    =         x = 8      x = 8
        //    +=       x += 3     x = x + 3
        //    -=       x -= 3     x = x - 3
        //    *=       x *= 3     x = x * 3
        //    /=       x /= 3     x = x / 3
        //    %=       x %= 3     x = x % 3
        int yas=33;
        System.out.println(yas+5);//38-> atama (=) olmaıdğı için yas hala 33
        System.out.println("yas = "+ yas);//yas=33
        System.out.println(yas+=11);//44-> atama yapıldığı için yaş 44'tür.

    }
}
